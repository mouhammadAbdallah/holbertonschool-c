#include "3-calc.h"

/**
 * op_add - op_add
 * @a: nb
 * @b: nb
 *
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - op_sub
 * @a: nb
 * @b: nb
 *
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - op_mul
 * @a: nb
 * @b: nb
 *
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - op_div
 * @a: nb
 * @b: nb
 *
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - op_mod
 * @a: nb
 * @b: nb
 *
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
